/*
 *  JS declaração de Variaveis e Constantes
 */

// Declarar com var a variavel fica exposta em toda aplicacao (Contexto window no JS e global no Node)
var a = 5;

// Declarar com let é o novo mode de declarar variavel, e ela se comporta limitada aou seu contexto
let b = 6;

//Declarar com const a variavel é uma CONSTANTE
const c = 99;
