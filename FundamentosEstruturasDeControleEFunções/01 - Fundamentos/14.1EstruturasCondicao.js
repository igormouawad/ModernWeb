const x = 5;

//IF
if (x > 3) {
    console.log("x é maior que 3");
}

//IF/ELSE
if (x > 7) {
    console.log("x é maior que 5");

} else {
    console.log("x é menor que 5");
}

//IF/IFELSE/ELSE
if (x > 5) {
    console.log("x é maior que 5");

} else if (x == 5) {
    console.log("x é igual a 5");
} else {
    console.log("x é menor que 5");
}

//SWITCH
switch (x) {
    case 1 : console.log("case 1");break;
    case 2 : console.log("case 2");break;
    case 3 : console.log("case 3");break;
    case 4 : console.log("case 4");break;
    case 5 : console.log("case 5");break;
    case 6 : console.log("case 6");break;
}